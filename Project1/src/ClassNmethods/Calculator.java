package ClassNmethods;

public class Calculator  {

	public int addition(int num1, int num2) {
		int total = 0;
		total = num1 + num2;
		return total;
				
	}
	public int subtraction(int num1, int num2) {
		int total = 0;
		total = num1 - num2;
		return total;
}
	public int multi(int num1, int num2) {
		int total = 0;
		total = num1 * num2;
		return total;
	
}
	public int div(int num1, int num2) {
		int total = 0;
		total = num1 / num2;
		return total;
	
  }
	
}	